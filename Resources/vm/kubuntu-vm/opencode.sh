#!/usr/bin/bash
qemu-system-x86_64 \
    -name opencode,process=opencode,debug-threads=on \
    -machine q35,smm=off,vmport=off,accel=kvm \
    -global kvm-pit.lost_tick_policy=discard \
    -cpu host \
    -smp cores=2,threads=2,sockets=1 \
    -m 8G \
    -device virtio-balloon \
    -pidfile opencode.pid \
    -rtc base=utc,clock=host \
    -device virtio-vga-gl,xres=1280,yres=800 \
    -display gtk,zoom-to-fit=off,gl=on \
    -device virtio-rng-pci,rng=rng0 \
    -object rng-random,id=rng0,filename=/dev/urandom \
    -device usb-ehci,id=input \
    -device usb-kbd,bus=input.0 \
    -k en-us \
    -device usb-tablet,bus=input.0 \
    -audiodev pipewire,id=audio0 \
    -device intel-hda \
    -device hda-micro,audiodev=audio0 \
    -device virtio-net-pci,netdev=nic \
    -netdev user,net=10.0.3.0/24,hostfwd=tcp::22220-:22,id=nic \
    -global driver=cfi.pflash01,property=secure,value=on \
    -drive if=pflash,format=raw,unit=0,file=/usr/share/OVMF/OVMF_CODE_4M.fd,readonly=on \
    -drive if=pflash,format=raw,unit=1,file=OVMF_VARS.fd \
    -device virtio-blk-pci,drive=SystemDisk \
    -drive id=SystemDisk,if=none,format=qcow2,file=opencode.qcow2,discard=unmap,detect-zeroes=unmap,cache=writeback,aio=threads \
    -monitor unix:opencode-monitor.socket,server,nowait \
    -serial unix:opencode-serial.socket,server,nowait 2>/dev/null
