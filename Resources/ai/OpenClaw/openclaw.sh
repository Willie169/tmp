#!/usr/bin/env bash
/bin/qemu-system-x86_64 \
    -name openclaw,process=openclaw,debug-threads=on \
    -machine q35,smm=off,vmport=off,accel=kvm \
    -global kvm-pit.lost_tick_policy=discard \
    -cpu host \
    -smp cores=2,threads=2,sockets=1 \
    -m 8G \
    -device virtio-balloon \
    -pidfile openclaw.pid \
    -rtc base=utc,clock=host \
    -device virtio-vga-gl,xres=1280,yres=800 \
    -display gtk,grab-on-hover=on,zoom-to-fit=off,gl=on \
    -device virtio-rng-pci,rng=rng0 \
    -object rng-random,id=rng0,filename=/dev/urandom \
    -device qemu-xhci,id=spicepass \
    -chardev spicevmc,id=usbredirchardev1,name=usbredir \
    -device usb-redir,chardev=usbredirchardev1,id=usbredirdev1 \
    -chardev spicevmc,id=usbredirchardev2,name=usbredir \
    -device usb-redir,chardev=usbredirchardev2,id=usbredirdev2 \
    -chardev spicevmc,id=usbredirchardev3,name=usbredir \
    -device usb-redir,chardev=usbredirchardev3,id=usbredirdev3 \
    -device pci-ohci,id=smartpass \
    -device usb-ccid \
    -chardev spicevmc,id=ccid,name=smartcard \
    -device ccid-card-passthru,chardev=ccid \
    -device usb-ehci,id=input \
    -device usb-kbd,bus=input.0 \
    -k en-us \
    -device usb-tablet,bus=input.0 \
    -audiodev pipewire,id=audio0 \
    -device intel-hda \
    -device hda-micro,audiodev=audio0 \
    -device virtio-net-pci,netdev=nic \
    -netdev user,hostname=openclaw,net=10.0.3.0/24,hostfwd=tcp::2222-:22,id=nic \
    -global driver=cfi.pflash01,property=secure,value=on \
    -drive if=pflash,format=raw,unit=0,file=/usr/share/OVMF/OVMF_CODE_4M.fd,readonly=on \
    -drive if=pflash,format=raw,unit=1,file=OVMF_VARS.fd \
    -device virtio-blk-pci,drive=SystemDisk \
    -drive id=SystemDisk,if=none,format=qcow2,file=openclaw.qcow2,discard=unmap,detect-zeroes=unmap,cache=writeback,aio=threads \
    -fsdev local,id=fsdev0,path=/home/willie/Public,security_model=mapped-xattr \
    -device virtio-9p-pci,fsdev=fsdev0,mount_tag=Public-willie \
    -monitor unix:openclaw-monitor.socket,server,nowait \
    -serial unix:openclaw-serial.socket,server,nowait 2>/dev/null
